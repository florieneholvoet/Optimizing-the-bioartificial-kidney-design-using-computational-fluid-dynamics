def concentration_at_r(R, C, r0):

    R_np = np.asarray(R, dtype=float)
    C_np = np.asarray(C, dtype=float)

    idx = np.argsort(R_np)

    R_np = R_np[idx]
    C_np = C_np[idx]

    exact = np.where(np.isclose(R_np, r0, atol=1e-12))[0]

    if len(exact) > 0:
        return C_np[exact[0]]

    return np.interp(r0, R_np, C_np)


