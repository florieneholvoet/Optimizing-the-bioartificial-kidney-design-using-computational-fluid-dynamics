def read_comsol_txt(filepath):

    header_line = None
    data_lines = []

    with open(filepath) as f:
        for line in f:

            if line.startswith("%"):
                candidate = line.strip("%").strip()

                if candidate.startswith("r") and "@ t=" in candidate:
                    header_line = candidate
                continue

            data_lines.append(line.strip())

    columns = re.split(r"\s{2,}", header_line)

    rows = []
    for line in data_lines:
        parts = re.split(r"\s+", line)
        if len(parts) == len(columns):
            rows.append(parts)

    df = pd.DataFrame(rows, columns=columns).astype(float)

    time_map = {}

    for col in df.columns:
        match = re.search(r"@ t=([0-9.]+)", col)
        if match:
            t = float(match.group(1))
            time_map[t] = col

    df = df.sort_values("r")

    return df, time_map


# ============================================================
