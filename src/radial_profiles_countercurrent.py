# Difference = concentration at r = 1.8 mm - concentration at r = 0.17 mm
# ============================================================

def print_blood_to_membrane_difference(filepath):

    df, time_map = read_comsol_txt(filepath)

    z = re.search(r'z=([0-9.]+)', filepath.name).group(1)

    print("\n" + "=" * 70)
    print(f"Blood-to-membrane concentration difference for z = {z} mm")
    print("Difference = C(r = 1.8 mm) - C(r = 0.17 mm)")
    print("=" * 70)

    for t in TIMES:
        if t in time_map:
            col = time_map[t]

            c_blood = df.loc[(df["r"] - 1.8).abs().idxmin(), col]
            c_membrane = df.loc[(df["r"] - 0.17).abs().idxmin(), col]

            difference = c_blood - c_membrane

            print(
                f"t = {t:>4} min | "
                f"C(1.8 mm) = {c_blood:.6e} | "
                f"C(0.17 mm) = {c_membrane:.6e} | "
                f"Difference = {difference:.6e}"
            )


