# READ COMSOL FILE
# =========================
df = pd.read_csv(
    file_path,
    comment="%",
    sep=r"\s+",
    header=None
)

# Create column names
# First 2 columns = R/Z
# Remaining columns = pressure at different times
ncols = df.shape[1]

time_columns = [f"p_t{i}" for i in range(ncols - 2)]

df.columns = ["R", "Z"] + time_columns

# =========================
# USE PRESSURE AT t = 0
# =========================
plot_df = df[["Z", "p_t0"]].dropna()

# Sort by axial coordinate
plot_df = plot_df.sort_values("Z")

# Shift Z so arc length starts at 0
plot_df["ArcLength"] = plot_df["Z"] - plot_df["Z"].min()

# =========================
# CREATE FIGURE
# =========================
fig, ax = plt.subplots(figsize=(12, 6))

# Plot
ax.plot(
    plot_df["ArcLength"],
    plot_df["p_t0"],
    color="#5b4b3a",
    linewidth=2
)

# =========================
# TITLES & LABELS
# =========================
ax.set_title(
    "Pressure (Pa)",
    fontsize=24
)

ax.set_xlabel(
    "Arc length (mm)",
    fontsize=20
)

ax.set_ylabel(
    "Pressure (Pa)",
    fontsize=20
)

