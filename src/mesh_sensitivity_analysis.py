def read_comsol_single_flux_table(filepath):
    """
    Reads COMSOL text export with:
    Time (min) + 1 flux column

    Returns dataframe with columns:
    ['Time (min)', 'Flux']
    """
    data = []

    with open(filepath, "r", encoding="utf-8") as f:
        for line in f:
            stripped = line.strip()

            if not stripped or stripped.startswith("%"):
                continue

            values = [float(x) for x in stripped.split()]
            data.append(values)

    if len(data) == 0:
        raise ValueError(f"No numeric data found in {filepath}")

    ncols = len(data[0])
    if ncols != 2:
        raise ValueError(f"{filepath} should have 2 columns, found {ncols}")

    df = pd.DataFrame(data, columns=["Time (min)", "Flux"])
    return df


def read_comsol_probe_table(filepath):
    with open(filepath, "r", encoding="utf-8") as f:
        lines = f.readlines()

    header_line = None
    data_lines = []

    for line in lines:
        stripped = line.strip()

        if stripped.startswith("%"):
            if "Time (min)" in stripped:
                header_line = stripped.lstrip("%").strip()
        else:
            if stripped:
                data_lines.append(stripped)

    if header_line is None:
        raise ValueError(f"Could not find header line in {filepath}")

    pattern = r"(Time \(min\)|Molar concentration, is \(mol/m\^3\), Point: \([^)]+\))"
    columns = re.findall(pattern, header_line)

    seen = {}
    unique_columns = []
    for col in columns:
        if col not in seen:
            seen[col] = 0
            unique_columns.append(col)
        else:
            seen[col] += 1
            unique_columns.append(f"{col}__dup{seen[col]}")

    data = []
    for line in data_lines:
        values = [float(x) for x in line.split()]
        if len(values) == len(unique_columns):
            data.append(values)

    df = pd.DataFrame(data, columns=unique_columns)
    return df


