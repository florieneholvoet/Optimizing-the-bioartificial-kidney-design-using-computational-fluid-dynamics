def extract_point(colname):
    clean_col = colname.split("__dup")[0]
    match = re.search(r"Point:\s*(\([^)]+\))", clean_col)
    return match.group(1) if match else "Unknown"


def filter_times(df, requested_times, time_column, tol=1e-9):
    rows = []
    for t in requested_times:
        matches = np.isclose(df[time_column], t, atol=tol)
        idx = np.where(matches)[0]
        if len(idx) == 0:
            raise ValueError(f"Time {t} not found in {time_column}")
        rows.append(df.iloc[idx[0]])

    out = pd.DataFrame(rows).reset_index(drop=True)
    out[time_column] = requested_times
    return out


def load_flux_series(filepath, requested_times):
    df = read_comsol_single_flux_table(filepath)
    df = filter_times(df, requested_times, "Time (min)")
    return df["Flux"].to_numpy()


def compute_relative_differences(reference_values, comparison_values):
    return np.where(
        np.isclose(reference_values, 0, atol=1e-30),
        np.nan,
        (reference_values - comparison_values) / reference_values
    )


def plot_flux_group(files, reference_file, times_to_keep, title):
    """
    Compare files with a single-flux reference file and plot:
    one figure, one curve per selected time
    """
    all_data = {}

    for fname in files:
        values = load_flux_series(fname, times_to_keep)
        all_data[fname] = values

    ref_values = load_flux_series(reference_file, times_to_keep)

    rel_diff = {}
    for fname in files:
        rel_diff[fname] = compute_relative_differences(ref_values, all_data[fname])

    x = np.arange(1, len(files) + 1)
    diff_matrix = np.array([rel_diff[f] for f in files])   # shape: (nfiles, ntimes)
    diff_percent = 100 * diff_matrix

    plt.figure(figsize=(11, 5))

    for t_idx, t in enumerate(times_to_keep):
        plt.plot(
            x,
            diff_percent[:, t_idx],
            marker="o",
            linewidth=2,
            label=f"{t} min"
        )

    plt.xticks(x, x_labels, rotation=20)
    plt.xlabel("Mesh comparison")
    plt.ylabel("Percentual difference (%)")
    plt.title(title)

    data_min = np.nanmin(diff_percent)
    data_max = np.nanmax(diff_percent)
    data_range = data_max - data_min

    if not np.isfinite(data_range) or data_range == 0:
        margin = 0.001
    else:
        margin = 0.2 * data_range

    plt.ylim(data_min - margin, data_max + margin)
    plt.gca().yaxis.set_major_formatter(FormatStrFormatter('%.4f'))

    plt.axhline(100 * threshold, color="red", linestyle="--", linewidth=1.5, label="±2% threshold")
    plt.axhline(-100 * threshold, color="red", linestyle="--", linewidth=1.5)

    plt.grid(True, linestyle="--", alpha=0.6)
    plt.legend()
    plt.tight_layout()
    plt.show()

    # Optional debug print
    print("\n============================================================")
    print(title)
    print(f"Reference file: {reference_file}")
    print("Formula       : (r - a) / r")
    print("Times kept    :", times_to_keep)
    print("============================================================")

    for fname in files:
        print(f"\nComparing {fname} to {reference_file}")
        a_vals = all_data[fname]

        for t, a, r in zip(times_to_keep, a_vals, ref_values):
            diff = (r - a) / r if abs(r) > 0 else np.nan
            print(
                f"t={t:>4} min | "
                f"a={a:.17e} | "
                f"r={r:.17e} | "
                f"(r-a)/r={diff:.17e}"
            )


def plot_probe_group(files, reference_file, times_to_keep, time_col):
    """
    Compare probe-point files with the probe reference file.
    One figure per probe point, one curve per selected time.
    """
    all_data = {}

    for fname in files:
        df = read_comsol_probe_table(fname)
        df = filter_times(df, times_to_keep, time_col)
        all_data[fname] = df

    ref_df = read_comsol_probe_table(reference_file)
    ref_df = filter_times(ref_df, times_to_keep, time_col)

    molar_cols = [c for c in ref_df.columns if c != time_col]

    relative_diff = {}
    for fname in files:
        relative_diff[fname] = {}
        for col in molar_cols:
            r = ref_df[col].to_numpy()
            a = all_data[fname][col].to_numpy()
            relative_diff[fname][col] = compute_relative_differences(r, a)

    x = np.arange(1, len(files) + 1)

    for col in molar_cols:
        point = extract_point(col)

        diff_matrix = np.array([
            relative_diff[f][col] for f in files
        ])
        diff_percent = 100 * diff_matrix

        plt.figure(figsize=(10, 5))

        for t_idx, t in enumerate(times_to_keep):
            plt.plot(
                x,
                diff_percent[:, t_idx],
                marker="o",
                linewidth=2,
                label=f"{t} min"
            )

        plt.xticks(x, x_labels, rotation=20)
        plt.xlabel("Mesh comparison")
        plt.ylabel("Percentual difference (%)")
        plt.title(f"Percentual difference at {point}")

        data_min = np.nanmin(diff_percent)
        data_max = np.nanmax(diff_percent)
        data_range = data_max - data_min

        if not np.isfinite(data_range) or data_range == 0:
            margin = 0.001
        else:
            margin = 0.2 * data_range

        plt.ylim(data_min - margin, data_max + margin)
        plt.gca().yaxis.set_major_formatter(FormatStrFormatter('%.4f'))

        plt.axhline(100 * threshold, color="red", linestyle="--", linewidth=1.5, label="±2% threshold")
        plt.axhline(-100 * threshold, color="red", linestyle="--", linewidth=1.5)

        plt.grid(True, linestyle="--", alpha=0.6)
        plt.legend()
        plt.tight_layout()
        from matplotlib.ticker import ScalarFormatter

        ax = plt.gca()

        formatter = ScalarFormatter(useMathText=True)
        formatter.set_scientific(True)
        formatter.set_powerlimits((-2, 2))  # force scientific notation

        ax.yaxis.set_major_formatter(formatter)
        plt.show()


