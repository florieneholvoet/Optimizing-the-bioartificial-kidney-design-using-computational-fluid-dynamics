file_flux_OIadjusted_baseline = "totalnormalflux_OIadjusted_baseline_bm.txt"

# ============================================================
# 3) HELPER FUNCTIONS
# ============================================================

def cumulative_trapezoid_no_scipy(y, x):
    out = np.zeros_like(x, dtype=float)

    for i in range(1, len(x)):
        dx = x[i] - x[i - 1]
        out[i] = out[i - 1] + 0.5 * (y[i] + y[i - 1]) * dx

    return out


def load_two_column_file(filepath):
    data = np.loadtxt(filepath, comments="%")

    time_min = data[:, 0]
    values = data[:, 1]

    return time_min, values


def compute_running_average_clearance(time_min, flux_mol_s, config_name):

    time_sec = time_min * 60.0

    cumulative_flux_mol = cumulative_trapezoid_no_scipy(
        flux_mol_s,
        time_sec
    )

    CL_running_m_s = np.zeros_like(time_sec, dtype=float)

    nonzero = time_sec > 0

    # Use configuration-specific area
    A_m2 = area_m2_by_config[config_name]

    CL_running_m_s[nonzero] = cumulative_flux_mol[nonzero] / (
        time_sec[nonzero] * A_m2 * C_in
    )

    CL_running_uL_min_cm2 = np.abs(
        CL_running_m_s * mps_to_uL_min_cm2
    )

    return CL_running_uL_min_cm2


def restrict_to_240min(time_min, values):
    mask = time_min <= T_min
    return time_min[mask], values[mask]


def save_current_figure(filename):
    plt.tight_layout()

    plt.savefig(
        os.path.join(OUTPUT_DIR, filename),
        dpi=300
    )

    plt.show()

# ============================================================
