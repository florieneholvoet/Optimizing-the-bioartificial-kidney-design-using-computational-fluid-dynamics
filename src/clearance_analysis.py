# ============================================================
# 1) USER INPUTS / CONSTANTS
# ============================================================

C_in = 0.1

# Configuration-specific membrane areas
# Computed from A_outer = 2*pi*Ro*L, with L = 20 mm
area_mm2_by_config = {
    "Inside-out": 31.42,
    "Outside-in": 21.36,
    "Adjusted outside-in": 198.23,
}

# Convert mm² -> m²
area_m2_by_config = {
    config: area_mm2 * 1e-6
    for config, area_mm2 in area_mm2_by_config.items()
}

T_min = 240
mps_to_uL_min_cm2 = 6e6

