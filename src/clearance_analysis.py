def load_flux_file_auto_time(filepath):
    data = np.loadtxt(filepath, comments="%")

    time_raw = data[:, 0]
    flux_mol_s = data[:, 1]

    # If time looks like seconds, convert to minutes
    if time_raw[-1] > 1000:
        time_min = time_raw / 60.0
    else:
        time_min = time_raw

    return time_min, flux_mol_s


def compute_model_derived_clearance(filepath, config_name):
    time_min, flux_mol_s = load_flux_file_auto_time(filepath)

    time_sec = time_min * 60.0
    total_mol = np.trapz(flux_mol_s, time_sec)

    # Use configuration-specific area
    A_m2 = area_m2_by_config[config_name]

    CL_model_m_s = total_mol / (time_sec[-1] * A_m2 * C_in)
    CL_model_uL_min_cm2 = abs(CL_model_m_s * mps_to_uL_min_cm2)

    return CL_model_uL_min_cm2


