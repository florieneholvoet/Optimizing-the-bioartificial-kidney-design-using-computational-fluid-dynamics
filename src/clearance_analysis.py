def load_flux_file_auto_time(filepath):
    data = np.loadtxt(filepath, comments="%")

    time_raw = data[:, 0]
    flux_mol_s = data[:, 1]

    if time_raw[-1] > 1000:
        time_min = time_raw / 60.0
    else:
        time_min = time_raw

    return time_min, flux_mol_s


def compute_model_derived_clearance_no_area(filepath):
    time_min, flux_mol_s = load_flux_file_auto_time(filepath)

    time_sec = time_min * 60.0
    total_mol = np.trapz(flux_mol_s, time_sec)

    # No membrane area normalization
    # Average volumetric clearance:
    # CL = total transported amount / (time * inlet concentration)
    CL_model_m3_s = total_mol / (time_sec[-1] * C_in)

    CL_model_uL_min = abs(CL_model_m3_s * m3s_to_uL_min)

    return CL_model_uL_min


def save_current_figure(filename):
    plt.tight_layout()
    plt.savefig(os.path.join(OUTPUT_DIR, filename), dpi=300)
    plt.show()


def add_bar_labels(bars, values):
    max_value = max(values)

    for bar, value in zip(bars, values):
        plt.text(
            bar.get_x() + bar.get_width() / 2,
            bar.get_height() + 0.02 * max_value,
            f"{value:.3e}",
            ha="center",
            va="bottom",
            fontsize=9,
        )

